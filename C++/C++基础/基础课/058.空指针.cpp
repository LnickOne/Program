#include <iostream>
using namespace std;

int main() // 空指针
{
	// 1、空指针用于给指针变量进行初始化
	// 指针变量p指向内存地址编号为0的空间
	//  int * p;//指针指向哪？未知！所以，一般会让指针指向NULL（空）
	int *p = NULL; // 指针变量p指向内存地址编号为0的空间

	// 2、空指针是不可以进行访问的
	// 0~255之间的内存编号是系统占用的，因此不可以访问
	// 下面的代码会导致程序崩溃，已注释掉
	// *p = 100; // 错误：不能对空指针进行解引用操作
	// 访问空指针会导致未定义行为（通常是程序崩溃）
	// 内存编号0 ~255为系统占用内存，不允许用户访问
	// cout << *p << endl; // 错误：不能对空指针进行解引用操作

	cout << "注意：空指针不能进行解引用操作！" << endl;
	return 0;
}