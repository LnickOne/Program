#include <iostream>
using namespace std;

int main()
{
	// 1、指针的定义
	int a = 10; // 定义整型变量a

	// 指针定义的语法：数据类型 * 指针变量名 ;
	int *p; // 定义整型指针变量p

	// 指针变量赋值：让指针记录变量a的地址
	p = &a;															  // 指针p指向变量a的地址（取地址运算符&用于获取变量的地址）
	cout << "a的地址为：" << &a << endl;							  // 打印变量a的地址（十六进制格式）
	cout << "a的地址为：" << reinterpret_cast<uintptr_t>(&a) << endl; // 安全地将地址转换为整型显示
	cout << "指针p的值为：" << p << endl;							  // 打印指针变量p的值（即a的地址）

	// 2、指针的使用
	// 可以通过"解引用"操作（*）来访问指针指向的内存中的值
	cout << "*p = " << *p << endl; // 通过解引用访问指针p指向的内存中的值（即a的值）

	// 通过指针修改指向的内存中的值
	*p = 1000;							   // 通过解引用操作修改指针p指向的内存中的值（即修改a的值）
	cout << "修改后，a = " << a << endl;   // 验证a的值已被修改
	cout << "修改后，*p = " << *p << endl; // 再次通过指针访问修改后的值
	return 0;
}